
package exercici1;

public class Operacions 
{
    public float suma;
    public float resta;
    public float mult;
    public float divisio;

    public float getSuma() {
        return this.suma;
    }

    public void setSuma(float suma) {
        this.suma = suma;
    }

    public float getResta() {
        return this.resta;
    }

    public void setResta(float resta) {
        this.resta = resta;
    }

    public float getMult() {
        return this.mult;
    }

    public void setMult(float mult) {
        this.mult = mult;
    }

    public float getDivisio() {
        return this.divisio;
    }

    public void setDivisio(float divisio) {
        this.divisio = divisio;
    }
    
    
}
